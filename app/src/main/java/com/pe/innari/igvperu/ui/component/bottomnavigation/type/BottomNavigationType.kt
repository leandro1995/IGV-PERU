package com.pe.innari.igvperu.ui.component.bottomnavigation.type

/**
 * Define los tipos de navegación disponibles en el componente.
 */
enum class BottomNavigationType {
    /** Navegación inferior estándar para pantallas compactas. */
    BOTTOM_NAVIGATION_BAR,

    /** Navegación lateral (riel) para pantallas anchas o apaisadas. */
    BOTTOM_NAVIGATION_RAIL
}
